
<center><h1>Audi A4 Avant 1.6 Benzin</h1></center>
<p>Az Audi 80 (1994 óta: A4) 1972 nyarának vége óta az Audi középkategóriás modellsorozatának a neve. Házon belül ezt a sorozatot "B-típusnak" is nevezik.</p>
<p>Az előd modell az Audi F103 széria volt, amelyet lóerőteljesítménye alapján neveztek el Audi 60, 75, 80 és Super 90. Az Audi 80 elnevezést ezután a teljes modellsorozatra átvették és 1994 őszén az A4 váltotta fel.</p>
<p>Az A4 a cég legtöbbször gyártott modellje és 2007-ben a német Forgalomba helyezési statisztikák 4. helyén állt a VW Golf, a VW Passat és a BMW 3-as autók után. A4-est vásárló magán/privát ember kevés volt. 2014-ben a Németországban eladott Audi A4-ek 88,1%-át kereskedelmi forgalomba helyezték. A dízelmotoros járművek aránya 2014-ben 87,6% volt.</p>
<p>2011 októberében elkészült a B sorozat tízmilliomodik járműve.</p>
<p>A 2015-ös IAA kiállításon mutatta be az Audi az Audi A4 B9-et, amely röviddel ezután került forgalomba. Először fordult elő olyan, hogy a szedán és a kombit is egyszerre mutatták be.</p>   


